    thinkingBtn: "思考中...",
    loadingText: "DeepSeek がドキュメントを分析して回答を生成中...",
    errorPrefix: "エラー：",
    sourcesTitle: "📑 参照ドキュメント：",
    aiNotice: "結果は REBOCAP 公式ドキュメントに基づいて AI が生成した参考情報です",
