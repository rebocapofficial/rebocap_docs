    thinkingBtn: "思考中...",
    loadingText: "DeepSeek 正在分析知識庫並生成解答...",
    errorPrefix: "請求出錯：",
    sourcesTitle: "📑 參考文檔：",
    aiNotice: "結果由 AI 基於 REBOCAP 官方文檔生成，僅供參考",
